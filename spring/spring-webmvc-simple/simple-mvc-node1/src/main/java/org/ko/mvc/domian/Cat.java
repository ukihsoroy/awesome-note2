package org.ko.mvc.domian;

public class Cat {
	
	private String name;
	
	private String age;
	
	private String kind;

	public String getName() {
		return name;
	}

	public String getAge() {
		return age;
	}

	public String getKind() {
		return kind;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}
	
	
}
